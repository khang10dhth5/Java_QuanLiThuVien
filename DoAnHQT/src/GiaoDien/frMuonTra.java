    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
         if(jTable1.getSelectedRowCount()<1){
                JOptionPane.showMessageDialog(this,"hãy chọn Phiếu muốn xóa!");
                
        }
        else if(ChiTietMTDAO.ktChiTietMTTheoMaMT(Integer.parseInt((String)jTable1.getValueAt(jTable1.getSelectedRow(), 0)))){
                JOptionPane.showMessageDialog(this, "Xóa không thành công do có chi tiết phiếu nhập tham chiếu đến");
                return;
        }
        else{
                int row=jTable1.getSelectedRow();
               String ma= (String)jTable1.getValueAt(row, 0);
        
                int tl=JOptionPane.showConfirmDialog(null,"bạn muốn xóa phải không","thông báo",JOptionPane.YES_NO_OPTION);
                  if(tl==JOptionPane.YES_OPTION){
                       MuonTraDAO.xoaMT(Integer.parseInt(ma));
                       JOptionPane.showMessageDialog(this,"Xóa thành công");
                      hienthi();
                      txtNgayMuon.setText("");
                      txtNgayTra.setText("");
                }
                else{
                    
                   return;
                
                }
         }
    }//GEN-LAST:event_btnXoaActionPerformed

    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if(cbo_DocGia.getSelectedIndex()==-1){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông rin bắt buộc!");
        }
        else{
            int tg=cbo_DocGia.getSelectedIndex();
            if(txtNgayMuon.getText().equals("")){
                MuonTra dg=new MuonTra(cbo_DocGia.getItemAt(tg).toString().substring(cbo_DocGia.getItemAt(tg).toString().lastIndexOf(':')+1),
                        txtMaQL.getText().substring(txtMaQL.getText().lastIndexOf(':')+1),txtNgayMuon.getText(),txtNgayTra.getText());
                MuonTraDAO.themMTKoNgayMuon(dg);
            }
            else if(txtNgayTra.getText().equals("")){
                MuonTra dg=new MuonTra(cbo_DocGia.getItemAt(tg).toString().substring(cbo_DocGia.getItemAt(tg).toString().lastIndexOf(':')+1),
                        txtMaQL.getText().substring(txtMaQL.getText().lastIndexOf(':')+1),txtNgayMuon.getText(),txtNgayTra.getText());
                MuonTraDAO.themMT(dg);
            }
            else{
                MuonTra dg=new MuonTra(cbo_DocGia.getItemAt(tg).toString().substring(cbo_DocGia.getItemAt(tg).toString().lastIndexOf(':')+1),
                        txtMaQL.getText().substring(txtMaQL.getText().lastIndexOf(':')+1),txtNgayMuon.getText(),txtNgayTra.getText());


                MuonTraDAO.themMTCoNgayTra(dg);
            }

            cbo_DocGia.setSelectedIndex(-1);
            lb_ma.setText("");
            txtMaQL.setText(""); 
            txtNgayMuon.setText(""); 
            txtNgayTra.setText("");
            btnLuu.setVisible(false);
            btnHuy.setVisible(false);
            btnCapNhat.setVisible(true);
            btnXoa.setVisible(true);
            hienthi();
            lb_k1.setVisible(false);
            lb_k2.setVisible(false);
        }
        
        
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        int tg=cbo_DocGia.getSelectedIndex();
        if(txtNgayTra.getText().equals("")){
            MuonTra dg=new MuonTra(lb_ma.getText(),cbo_DocGia.getItemAt(tg).toString().substring(cbo_DocGia.getItemAt(tg).toString().lastIndexOf(':')+1),
                    txtMaQL.getText().substring(txtMaQL.getText().lastIndexOf(':')+1),txtNgayMuon.getText(),txtNgayTra.getText());
            int tl=JOptionPane.showConfirmDialog(this, "bn có muốn cập nhật nxb có mã "+lb_ma.getText(), "thông báo", JOptionPane.YES_NO_OPTION);
            if(tl==JOptionPane.YES_OPTION){
                MuonTraDAO.capNhatMT(dg);
            hienthi();
            //sau khi cập nhật vẫn hiện ĐỘC GIẢ
             DocGia a=DocGiaDAO.layDG(Integer.parseInt(cbo_DocGia.getItemAt(tg).toString().substring(cbo_DocGia.getItemAt(tg).toString().lastIndexOf(':')+1)));
            int t2=Integer.parseInt(a.getMaDG());
            DocGia  x2=DocGiaDAO.layDG(t2);
            cbo_DocGia.setSelectedItem(x2.toString());
            }
            else return;
        }
        else{
            MuonTra dg=new MuonTra(lb_ma.getText(),cbo_DocGia.getItemAt(tg).toString().substring(cbo_DocGia.getItemAt(tg).toString().lastIndexOf(':')+1),
                    txtMaQL.getText().substring(txtMaQL.getText().lastIndexOf(':')+1),txtNgayMuon.getText(),txtNgayTra.getText());
            int tl=JOptionPane.showConfirmDialog(this, "bn có muốn cập nhật nxb có mã "+lb_ma.getText(), "thông báo", JOptionPane.YES_NO_OPTION);
            if(tl==JOptionPane.YES_OPTION){
                MuonTraDAO.capNhatMTCoNgayTra(dg);
            hienthi(); 
            //sau khi cập nhật vẫn hiện ĐỘC GIẢ
             DocGia a=DocGiaDAO.layDG(Integer.parseInt(cbo_DocGia.getItemAt(tg).toString().substring(0,1)));
            int t2=Integer.parseInt(a.getMaDG());
            DocGia  x2=DocGiaDAO.layDG(t2);
            cbo_DocGia.setSelectedItem(x2.toString());
            }
            else return;
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

