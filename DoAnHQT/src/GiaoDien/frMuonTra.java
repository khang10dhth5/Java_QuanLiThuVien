    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int row=jTable1.getSelectedRow();
        ma= (String)jTable1.getValueAt(row, 0);
        ChiTietMuonTra a=new ChiTietMuonTra(Integer.parseInt(ma));
        a.setVisible(true);
        MuonTra b=MuonTraDAO.layMT(Integer.parseInt(ma));
        lb_ma.setText(b.getMaMT());
        QuanLi v=QuanLiDAO.layQL(Integer.parseInt(b.getMaQL()));
        txtMaQL.setText(v.toString());
        txtNgayMuon.setText(b.getNgayMuon());
        txtNgayTra.setText(b.getNgayTra());
         //hiện cbo NXB
        DocGia nxb=DocGiaDAO.layDG(Integer.parseInt(b.getMaDG()));
        cbo_DocGia.setSelectedItem(nxb.toString());
    }//GEN-LAST:event_jTable1MouseClicked

