    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
        if(txtTen.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Vui lòng nhập đầy đủ thông tin bắt buộc");
        }
        else{
            TacGia dg=new TacGia(txtTen.getText(),txtThongTin.getText());
            TacGiaDAO.themTG(dg);
            lb_ma.setText("");
            txtTen.setText("");
            txtThongTin.setText("");  
            btnLuu.setVisible(false);
            btnHuy.setVisible(false);
            btnCapNhat.setVisible(true);
            btnXoa.setVisible(true);
            lb_k1.setVisible(false);
            lb_k2.setVisible(false);
            hienthi();
        }
        
       
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnCapNhatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCapNhatActionPerformed
        // TODO add your handling code here:
        if(txtTen.getText().equals("")){
            JOptionPane.showMessageDialog(this,"Vui lòng nhập đầy đủ thông tin bắt buộc");
        }
        else{
            TacGia dg=new TacGia(lb_ma.getText(),txtTen.getText(),txtThongTin.getText());
            int tl=JOptionPane.showConfirmDialog(this, "bn có muốn cập nhật TÁC GIẢ có mã "+lb_ma.getText(), "thông báo", JOptionPane.YES_NO_OPTION);
            if(tl==JOptionPane.YES_OPTION){
               TacGiaDAO.capNhatTG(dg);
            hienthi(); 
            }
            else return;
        }
    }//GEN-LAST:event_btnCapNhatActionPerformed

