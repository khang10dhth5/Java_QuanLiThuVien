    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        if(jTable1.getSelectedRowCount()<1){
            JOptionPane.showMessageDialog(this,"hãy chọn Tác giả muốn xóa!");
        }
        else {
            int row=jTable1.getSelectedRow();
            String ma= (String)jTable1.getValueAt(row, 0);

                  int tl=JOptionPane.showConfirmDialog(null,"bạn muốn xóa phải không","thông báo",JOptionPane.YES_NO_OPTION);
                    if(tl==JOptionPane.YES_OPTION){
                        TacGiaDAO.xoaTG(Integer.parseInt(ma));
                        hienthi();
                        JOptionPane.showMessageDialog(this,"Xóa thành công");
                        txtThongTin.setText("");
                        txtTen.setText("");
                    }
                    else{
                            return;
                        

             }
        }
    }//GEN-LAST:event_btnXoaActionPerformed

