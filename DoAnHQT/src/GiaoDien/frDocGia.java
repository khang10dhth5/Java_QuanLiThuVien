    private void hienthi() {
       ArrayList<DocGia> dsDG=DocGiaDAO.layDanhSachDG();
        dtm=(DefaultTableModel)jTable1.getModel();
        dtm.setRowCount(0);
        for(DocGia db:dsDG){
            Vector<Object> vec=new Vector<Object>();
            vec.add(db.getMaDG());
            vec.add(db.getTenDG());
            dtm.addRow(vec);
            
        }
        jTable1.setModel(dtm);
    }
