    private void btnTimKiemDocGiaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemDocGiaActionPerformed
        // TODO add your handling code here:
         ArrayList<DocGia> dsDG=DocGiaDAO.timKiemDGTheoTen(txtTimDocGia.getText());
        DefaultTableModel atm=(DefaultTableModel)jTable1.getModel();
        atm.setRowCount(0);
        for(DocGia db:dsDG){
            Vector<Object> vec=new Vector<Object>();
            vec.add(db.getMaDG());
            vec.add(db.getTenDG());
            vec.add(db.getSdt());
            vec.add(db.getDiaChi());
            vec.add(db.getSlmuon());
            atm.addRow(vec);
            
        }
        jTable1.setModel(atm);
    }//GEN-LAST:event_btnTimKiemDocGiaActionPerformed

