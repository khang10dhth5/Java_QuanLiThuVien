    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        // TODO add your handling code here:
         
       
        if(txtDiaChi.getText().equals("")||txtSDT.getText().equals("")||txtTen.getText().equals("")){
             JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin bắt buộc");
         }
        else{
             try{
                  Double a=Double.parseDouble(txtSDT.getText());
                  if(txtSLMuon.getText().equals("")){
                    DocGia dg=new DocGia(txtTen.getText(),txtSDT.getText(),txtDiaChi.getText(),0);    
                    DocGiaDAO.themDG(dg);
                     hienthi();
                     btnHuy.setVisible(false);
                    btnLuu.setVisible(false);
                    txtDiaChi.setText("");
                    txtSDT.setText("");
                    txtSLMuon.setText("0");
                    txtTen.setText("");
                    lb_Ma.setText("");
                    btnCapNhat.setVisible(true);
                    btnXoa.setVisible(true);
                    lb_k1.setVisible(false);
                    lb_k2.setVisible(false);
                    lb_k3.setVisible(false);
                    lb_k5.setVisible(false);
                    JOptionPane.showMessageDialog(this, "Thêm Thành Công");
        
                }
                else{
                    DocGia dg=new DocGia(txtTen.getText(),txtSDT.getText(),txtDiaChi.getText(),Integer.parseInt(txtSLMuon.getText()));    
                    DocGiaDAO.themDG(dg);
                     hienthi();
                     btnHuy.setVisible(false);
                    btnLuu.setVisible(false);
                    txtDiaChi.setText("");
                    txtSDT.setText("");
                    txtSLMuon.setText("0");
                    txtTen.setText("");
                    lb_Ma.setText("");
                    btnCapNhat.setVisible(true);
                    btnXoa.setVisible(true);
                    lb_k1.setVisible(false);
                    lb_k2.setVisible(false);
                    lb_k3.setVisible(false);
                    lb_k5.setVisible(false);
                    JOptionPane.showMessageDialog(this, "Thêm Thành Công");
                }
             }
             catch(Exception ex){
                 JOptionPane.showMessageDialog(this, "SDT Không hợp lệ");
                 txtSDT.requestFocus();
             }
        }
        
        
        
    }//GEN-LAST:event_btnLuuActionPerformed

    private void btnXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoaActionPerformed
        // TODO add your handling code here:
        
        
         if(jTable1.getSelectedRowCount()<1){
             JOptionPane.showMessageDialog(this, "Hãy chọn Độc giả muốn xóa");
         }
         else{
                int row=jTable1.getSelectedRow();
                ma= (String)jTable1.getValueAt(row, 0);
              int tl=JOptionPane.showConfirmDialog(null,"bạn muốn xóa phải không","thông báo",JOptionPane.YES_NO_OPTION);
                if(tl==JOptionPane.YES_OPTION){
                    DocGiaDAO.xoaDG(Integer.parseInt(ma));
                    hienthi();
                    JOptionPane.showMessageDialog(this, "Xóa Thành Công");
                    txtSDT.setText("");
                    txtSLMuon.setText("0");
                    txtTen.setText("");
                    txtDiaChi.setText("");
                }
                else{
                    return;
                }
         }
         
    }//GEN-LAST:event_btnXoaActionPerformed

